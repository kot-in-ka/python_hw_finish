# –ó–∞–¥–∞—á–∞ 1. –ü–æ—Å—Ç—Ä–æ–π—Ç–µ –≥—Ä–∞—Ñ–∏–∫ —Ñ—É–Ω–∫—Ü–∏–∏ ùëì(ùë•)=5ùë•^2+10ùë•‚àí30
# –ü–æ –≥—Ä–∞—Ñ–∏–∫—É –æ–ø—Ä–µ–¥–µ–ª–∏—Ç–µ, –ø—Ä–∏ –∫–∞–∫–∏—Ö –∑–Ω–∞—á–µ–Ω–∏—è x –∑–Ω–∞—á–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω–æ.
def task1():
    x = []
    y = []

    for i in range(-10, 10):
        x.append(i)
    for i in range(len(x)):
        y.append(5 * x[i] * x[i] + 10 * x[i] - 30)

    print(f'–ó–Ω–∞—á–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω–æ –Ω–∞ —É—á–∞—Å—Ç–∫–µ –º–µ–∂–¥—É —Ç–æ—á–∫–∞–º–∏ –ø–µ—Ä–µ—Å–µ—á–µ–Ω–∏—è –≥—Ä–∞—Ñ–∏–∫–∞ —Ñ—É–Ω–∫—Ü–∏–∏ —Å –æ—Å—å—é X')

    plt.title("–ì—Ä–∞—Ñ–∏–∫ —Ñ—É–Ω–∫—Ü–∏–∏ f(x)=5x^2+10x-30")
    plt.axhline(0, color='g')
    plt.axvline(0, color='g')
    plt.grid()
    plt.plot(x, y)
    plt.show()

